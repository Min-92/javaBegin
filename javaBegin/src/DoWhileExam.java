import java.util.Scanner;

public class DoWhileExam {

	public static void main(String[] args) {
		int value = 0;
		Scanner scan = new Scanner(System.in);
		
		do {
			//반복할 문장들
			value = scan.nextInt();
			System.out.println("입력받은 값 : ");
		}while(value != 10);
		
		System.out.println("반복은 종료!!");
	}
	

}
